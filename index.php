					<h1>Horizons</h1>
					<p class="credit"> Todd Detweiler, P2C </p>
				</header>
				
				<section>
					<p>When Bike & Build riders dip their tires in the Atlantic Ocean, they stand on the line that divides earth and sky.  Horizons are a picturesque, barefaced visualization of possibility, distance, and the unknown and as riders pause for this small ceremony it is easy to compare their backdrop to what lies ahead on the road.  Three months later, those same riders will run in to the Pacific together, and their sightline will never be as simple or as expressive.  Over the course of 4,000 hard pedaled miles, the horizon remained unchanged but the riders’ vantage point is new and raw in its strength- broadened and more deeply understood.</p>
 
<p>Our trips challenge riders to find the relationship between visible and true horizons.  The subject matter crosses nearly every realm - physical, integral, impact, and identity being just a few. “Can I ride 117 miles in a day?”, “Who knew I could learn so much from a Grandmother in Nebraska?”, “How and to whom can we best allocate the money we fundraised?”, “What is my influence now and what could it be?”, “What is next?”. </p>
 
<p>The answers to those questions compose Bike & Build’s enduring impact.   </p>
 
<p>The Bike & Build Board of Directors and staff are tremendously proud of the nearly 330 young adults who participated in Bike & Build in 2014.  We continue to be inspired by our riders who remain committed to a lifetime of service, the housing organizations and low-income families that we support, and the host communities that advocate with us. Every person touched by Bike & Build has their own vista, but the composition is the same: more affordable homes for our fellow citizens, and more opportunities for young adults to continue serving to make it so.</p>
 
<p>There are a wealth of vantage points in the thousands of miles between Atlantic and Pacific and we are excited to share with you just a few in this 2014 Annual Report. </p> 

