					<h6>From the Board and Staff</h6>
					<h1>Horizons</h1>
					<small> <span class="icon-camera"> </span> Todd Detweiler, P2C 2014 </small>
				</header>
				
				<section>
					<p>When Bike & Build riders dip their back tires in the Atlantic Ocean, they face a line that divides earth and sky.  The horizon is a picturesque visualization of possibility, as riders stand on the cusp of an unforgettable summer. What lies ahead, in the open road, is an adventure that will test their limits. Ultimately, when our riders crest the horizon that holds the Pacific in view, one summer and 4,000 miles later, they’ve broadened their own vantage points, many ready to serve their communities long after Bike & Build. </p>

					<p>Our trips challenge riders to find the relationship between visible and theoretical horizons.  The subject matter crosses nearly every realm - physical, integral, impact, and identity. “Can I ride 117 miles in a day?”, “Who knew I could learn so much from a Grandmother in Nebraska?”, “How and to whom can we best allocate the money we fundraised?”, “What is my influence now and what could it be?”, “What is next?”.</p>
 
					<p>The answers to those questions compose Bike & Build’s enduring impact.</p>   
 
					<p>The Bike & Build Board of Directors and staff are tremendously proud of the nearly 330 young adults who participated in Bike & Build in 2014. We aim to continue broadening their impact, as we are inspired by these young adults who are committed to a lifetime of service, the housing organizations and low-income families that we support, and the host communities that advocate with us. Each person and family touched by Bike & Build sees views their own horizon differently, but we share one a common goal: more affordable homes for our fellow citizens, and more opportunities for young adults to continue serving to make it so.</p> 
 
					<p>There are a wealth of vantage points in the thousands of miles between Atlantic and Pacific. We are excited to share with you just a few in our 2014 Annual Report.</p>  

					<p>
						Steve Truong<br>
						Justin Villere<br>
						Brendan Newman<br>
